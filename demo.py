    res = classify(ad["text"])
    got = res.get("category", "?").split(":")[0].strip()
    conf = res.get("confidence", 0)
    ok = got == ad["true"]
    correct += ok
    rows.append((ad["id"], ad["lang"], got, ad["true"], "да" if ok else "НЕТ", conf))

